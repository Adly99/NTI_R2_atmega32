/*****************************************************************/
/* Description    : This function used to Enable GIE			 */ 
/*																 */
/*					Inputs : void					  			 */
/*					Return : void		 					     */
/*****************************************************************/
/* Pre_condition  :  this function must be used after Interrupt  */
/*     				 initialized 							     */
/*****************************************************************/

void GIE_voidEnable()
{
	
	/***********************************************************/	
	/** !comment : From Data Sheet : To Enable GIE you must	   */
	/**            set Bit Number 7 in SREG REGISTER by 1      */
	/***********************************************************/

	SET_BIT(SREG , 7);
	
}

/*****************************************************************/
/* Description    : This function used to Disable GIE			 */ 
/*																 */
/*					Inputs : void					  			 */
/*					Return : void		 					     */
/*****************************************************************/
/* Pre_condition  :  This function must be used after Interrupt  */
/*     				 Initialized 							     */
/*****************************************************************/

void GIE_voidDisable()
{
	
	/***********************************************************/	
	/** !comment : From Data Sheet : To Disable GIE you must   */
	/**            cleare Bit Number 7 in SREG REGISTER by 0   */
	/***********************************************************/
	
	CLEAR_BIT(SREG , 7);
	
}



